import java.util.*;
class Pattern4{
	public static void main(String pp[]){
		Scanner p= new Scanner(System.in);
		int N=p.nextInt();
		for(int i=1;i<=N;i++){
			for(int j=1;j<=N;j++){
				System.out.print("1A ");
			}
			System.out.println();
		}
	}
}
